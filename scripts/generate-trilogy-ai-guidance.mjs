import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { isAbsolute, join, resolve } from 'node:path'
import { spawnSync } from 'node:child_process'
import { fileURLToPath } from 'node:url'

const repoRoot = resolve(fileURLToPath(new URL('..', import.meta.url)))
const outputPath = join(repoRoot, 'lib', 'llm', 'data', 'trilogySyntax.generated.ts')
const checkOnly = process.argv.includes('--check')

const virtualEnvPython =
  process.platform === 'win32' ? join('Scripts', 'python.exe') : join('bin', 'python')
const candidates = [
  process.env.PYTHON,
  process.env.VIRTUAL_ENV && join(process.env.VIRTUAL_ENV, virtualEnvPython),
  join(repoRoot, '.venv', virtualEnvPython),
  join(repoRoot, 'venv', virtualEnvPython),
  process.platform === 'win32' ? 'python.exe' : 'python3',
  'python',
].filter(Boolean)

const pythonSource = String.raw`
import json

import trilogy
from trilogy.ai.constants import AGGREGATE_FUNCTIONS, FUNCTIONS, RULE_PROMPT
from trilogy.ai.prompts import get_trilogy_syntax_reference
from trilogy.authoring import DataType

print(json.dumps({
    "sourceVersion": trilogy.__version__,
    "trilogySyntaxReference": get_trilogy_syntax_reference().strip(),
    "rulesInput": RULE_PROMPT.strip(),
    "aggFunctions": [line for line in AGGREGATE_FUNCTIONS.splitlines() if line],
    "functions": [line for line in FUNCTIONS.splitlines() if line],
    "datatypes": [value.value for value in DataType],
}, ensure_ascii=False))
`

let result
let python
const failures = []
for (const candidate of candidates) {
  if (isAbsolute(candidate) && !existsSync(candidate)) continue
  const attempt = spawnSync(candidate, ['-c', pythonSource], {
    cwd: repoRoot,
    encoding: 'utf8',
    env: { ...process.env, PYTHONIOENCODING: 'utf-8' },
    maxBuffer: 10 * 1024 * 1024,
  })
  if (attempt.status === 0) {
    result = attempt
    python = candidate
    break
  }
  failures.push(`${candidate}: ${attempt.error?.message || attempt.stderr.trim()}`)
}

if (!result) {
  console.error(
    'Unable to import pytrilogy. Install pyserver requirements in the repository virtualenv first.',
  )
  console.error(failures.join('\n'))
  process.exit(1)
}

const payload = JSON.parse(result.stdout)
const serialize = (value) => JSON.stringify(value, null, 2)
const generated = `// Generated by pnpm sync:trilogy-ai from pytrilogy ${payload.sourceVersion}.
// Do not edit this file directly.

export const pytrilogySyntaxVersion = ${serialize(payload.sourceVersion)}
export const trilogySyntaxReference = ${serialize(payload.trilogySyntaxReference)}
export const rulesInput = ${serialize(payload.rulesInput)}
export const aggFunctions = ${serialize(payload.aggFunctions)}
export const functions = ${serialize(payload.functions)}
export const datatypes = ${serialize(payload.datatypes)}
`

const current = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : ''
if (checkOnly) {
  if (current !== generated) {
    console.error(
      'Generated Trilogy AI guidance is stale. Run `pnpm sync:trilogy-ai` and commit the result.',
    )
    process.exit(1)
  }
  console.log(`Trilogy AI guidance matches pytrilogy ${payload.sourceVersion}.`)
} else {
  writeFileSync(outputPath, generated, 'utf8')
  console.log(`Generated ${outputPath} from pytrilogy ${payload.sourceVersion} using ${python}.`)
}
